const express = require("express");
const router = express.Router();
const createTask = require("./createTask");
const Task = require("../../models/tasksSchema");
const { fullUpdateTask, partialUpdateTask } = require("./updateTask");
const deleteTask = require("./deleteTask");
const { findAllTasks, findTaskById } = require("./findTask");
router.post("/", createTask(Task));
router.put("/:id", fullUpdateTask(Task));
router.patch("/:id", partialUpdateTask(Task));
router.delete("/:id", deleteTask(Task));
router.get("/", findAllTasks(Task));
router.get("/:id", findTaskById(Task));
module.exports = router;
